public class ExDataTypes {
	
	public static void main(String [] args) {
		
		//Default int, double
		
		//true, false
		
		boolean male = true;
		
		char letter = 'A';
		
		byte age = 20;
		short salary = 10500;
		int price = 150100;
		long distanceToMoon = 9223372036854775723L;
		
		
		float marksPercentile = 9.5f;
		double piValue = 3.53442343434;
		
		System.out.println(male);
		System.out.println(letter);
		System.out.println(age);
		System.out.println(salary);
		System.out.println(price);
		System.out.println(distanceToMoon);
		System.out.println(marksPercentile);
		System.out.println(piValue);		
		
	}
}